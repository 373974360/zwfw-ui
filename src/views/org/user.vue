<script>
    import {getDeptCascader} from 'api/org/dept';
    import {getUserList, updateUser, createUser, delUser} from 'api/org/user';
    import {copyProperties} from 'utils';
    import {mapGetters} from 'vuex';
    import TreeUtil from 'utils/TreeUtil.js';

    export default {
        name: 'table_demo',
        data() {
            const validatMobiles = (rule, value, callback) => {
                if (validatMobiles.test(value)) {
                    return callback(new Error('手机号码不正确'));
                }else {
                    callback();
                }
            }
            const validatePass2 = (rule, value, callback) => {
                if (value === '') {
                    callback(new Error('请再次输入密码'));
                } else if (value !== this.sysUser.password) {
                    callback(new Error('两次输入密码不一致!'));
                } else {
                    callback();
                }
            };
            return {
                list: null,
                total: null,
                listLoading: true,
                listQuery: {
                    page: this.$store.state.app.page,
                    rows: this.$store.state.app.rows,
                    deptName: undefined,
                    deptId: ''
                },
                sysUser: {
                    id: undefined,
                    deptId: '',
                    userName: '',
                    deptName: '',
                    sex: '',
                    phone: '',
                    avatar: '',
                    account: '',
                    password: '',
                    passwordConfirm: '',
                    enable: 1,
                    remark: '',
                    treePosition: '',
                    parentId: 0
                },
                sysUserRules: {
                    deptId: [
                        {required: true, message: '请选择部门', trigger: 'blur'}
                    ],
                    userName: [
                        {required: true, message: '请输入姓名', trigger: 'blur'}
                    ],
                    phone: [
                        {validator: validatMobiles, trigger: 'blur'}
                    ],
                    avatar: [
                        {type: 'url', required: true, message: '头像地址不正确', trigger: 'blur'}
                    ],
                    account: [
                        {required: true, message: '请输入账号', trigger: 'blur'}
                    ],
                    password: [
                        {required: true, message: '请输入密码', trigger: 'blur'},
                        {min: 6, max: 18, message: '长度在 6 到 18 个字符', trigger: 'blur'}
                    ],
                    passwordConfirm: [
                        {validator: validatePass2, trigger: 'blur'}
                    ]
                },
                selectedRows: [],
                cascader: [],
                dialogFormVisible: false,
                dialogStatus: '',
                dialogLoading: false

            }
        },
        computed: {
            cascaderModel: function () {
                if (this.sysUser.treePosition) {
                    const arr = this.sysUser.treePosition.split('&');
                    return arr;
                }
            },
            ...
                mapGetters([
                    'textMap',
                    'enums'
                ])
        },
        created()
        {
            this.getList();
            this.getOptions();
        }
        ,
        methods: {
            getOptions(id){
                this.dialogLoading = true;
                getDeptCascader(id).then(response => {
                    this.cascader = response.data;
                    this.dialogLoading = false;
                })
            },
            handleChange(value)
            {
                if(value.length > 0){
                    this.listQuery.deptId = value[value.length - 2];
                    this.sysUser.deptId = value[value.length - 2];
                    this.sysUser.parentId = value[value.length - 1];
                    this.sysUser.treePosition = value.join('&');
                }else{
                    this.listQuery.deptId = value[value.length - 2];
                    this.sysUser.parentId = value[value.length - 1];
                    this.sysRole.treePosition = undefined;
                }
            },
            getList() {
                this.listLoading = true;
                getUserList(this.listQuery).then(response => {
                    this.list = response.data.list;
                    this.total = response.data.total;
                    this.listLoading = false;
                })
            },
            handleSizeChange(val) {
                this.listQuery.rows = val;
                this.getList();
            },
            handleCurrentChange(val) {
                this.listQuery.page = val;
                this.getList();
            },
            handleSelectionChange(rows) {
                this.selectedRows = rows;
            },
            handleCreate(row) {
                this.currentRow = row;
                this.getOptions(null);
                this.dialogStatus = 'create';
                this.dialogFormVisible = true;
            },
            handleUpdate(row) {
                this.resetTemp();
                this.sysUser = copyProperties(this.sysUser, row);
                if (row._parent) {
                    this.sysUser.treePosition = row._parent.treePosition;
                } else {
                    this.sysUser.treePosition = undefined;
                }
                this.getOptions(this.sysUser.id);
                this.dialogStatus = 'update';
                this.dialogFormVisible = true;
            },
            handleDelete() {
                if (this.selectedRows.length == 0 ) {
                    this.$message.error('请选择需要操作的记录');
                } else {
                    this.$confirm('此操作将永久删除该信息, 是否继续?', '提示', {
                        confirmButtonText: '确定',
                        cancelButtonText: '取消',
                        type: 'warning'
                    }).then(() => {
                        let ids = new Array();
                        for (const deleteRow of this.selectedRows) {
                            ids.push(deleteRow.id);
                        }
                        delUser(ids).then(response => {
                            this.listLoading = false;
                            this.$message.success('删除成功');
                        })
                        for (const deleteRow of this.selectedRows) {
                            const index = this.list.indexOf(deleteRow);
                            this.list.splice(index, 1);
                        }
                    }).catch(() => {
                        console.dir('取消');
                    });
                }
            },
            create() {
                this.$refs['userForm'].validate(valid => {
                    if (valid) {
                        this.dialogFormVisible = false;
                        this.listLoading = true;
                        createUser(this.sysUser).then(response => {
                            TreeUtil.addRow(this.currentRow, response.data, this.list);
                            this.$message.success('创建成功');
                            this.listLoading = false;
                        })
                    } else {
                        return false;
                    }
                });
//                this.sysUser.id = parseInt(Math.random() * 100) + 1024;
//                this.sysUser.timestamp = +new Date();
//                this.sysUser.author = '原创作者';
//                this.list.unshift(this.sysUser);
//                this.dialogFormVisible = false;
//                this.$notify({
//                    title: '成功',
//                    message: '创建成功',
//                    type: 'success',
//                    duration: 2000
//                });
            },
            update() {
                this.$refs['userForm1'].validate(valid => {
                    if (valid) {
                        this.dialogFormVisible = false;
                        this.listLoading = true;
                        updateUser(this.sysUser).then(response => {
                            copyProperties(this.currentRow, response.data);
                            this.$message.success('更新成功');
                            this.listLoading = false;
                        })
                    } else {
                        return false;
                    }
                })
//                this.sysUser.timestamp = +this.sysUser.timestamp;
//                for (const v of this.list) {
//                    if (v.id === this.sysUser.id) {
//                        const index = this.list.indexOf(v);
//                        this.list.splice(index, 1, this.sysUser);
//                        break;
//                    }
//                }
//                this.dialogFormVisible = false;
//                this.$notify({
//                    title: '成功',
//                    message: '更新成功',
//                    type: 'success',
//                    duration: 2000
//                });
            },
            resetTemp() {
                this.sysUser = {
                    id: undefined,
                    deptId: '',
                    userName: '',
                    deptName: '',
                    sex: '',
                    phone: '',
                    avatar: '',
                    account: '',
                    password: '',
                    passwordConfirm: '',
                    enable: 1,
                    remark: '',
                    treePosition: '',
                    parentId: 0
                };
            },
            handleDownload() {
                require.ensure([], () => {
                    const {export_json_to_excel} = require('vendor/Export2Excel');
                    const tHeader = ['时间', '地区', '类型', '标题', '重要性'];
                    const filterVal = ['timestamp', 'province', 'type', 'title', 'importance'];
                    const data = this.formatJson(filterVal, this.list);
                    export_json_to_excel(tHeader, data, 'table数据');
                })
            },
            formatJson(filterVal, jsonData) {
                return jsonData.map(v => filterVal.map(j => {
                    if (j === 'timestamp') {
                        return parseTime(v[j])
                    } else {
                        return v[j]
                    }
                }))
            }
        }
    }
</script>
