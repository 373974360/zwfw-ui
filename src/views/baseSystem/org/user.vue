<template>
    <div class="app-container calendar-list-container">
        <div class="filter-container">
            <el-input @keyup.enter.native="getList" style="width: 130px;" class="filter-item" placeholder="姓名"
                      v-model="listQuery.name"></el-input>
            <el-cascader :options="cascader" class="filter-item" @change="handleChange"
                         :show-all-levels="true" clearable filterable expand-trigger="hover"
                         :change-on-select="true" style="width: 180px" placeholder="选择部门">
            </el-cascader>
            <el-button class="filter-item" type="primary" v-waves icon="search" @click="getList">搜索</el-button>
            <el-button class="filter-item" style="margin-left: 10px;" @click="handleCreate" type="primary" icon="plus">
                添加
            </el-button>
            <el-button class="filter-item" style="margin-left: 10px;" @click="handleDelete" type="danger" icon="delete">
                删除
            </el-button>
        </div>

        <el-table ref="userTable" :data="list" v-loading.body="listLoading" border fit highlight-current-row
                  style="width: 100%" @selection-change="handleSelectionChange" @row-click="toggleSelection">
            <el-table-column type="selection" width="55"/>
            <el-table-column align="center" label="序号">
                <template slot-scope="scope">
                    <span>{{scope.row.id}}</span>
                </template>
            </el-table-column>
            <el-table-column prop="name" align="center" label="姓名">
                <template slot-scope="scope">
                    <el-tooltip class="item" effect="dark" content="点击编辑" placement="right-start">
                        <span class="link-type" @click='handleUpdate(scope.row)'>{{scope.row.name}}</span>
                    </el-tooltip>
                </template>
            </el-table-column>
            <el-table-column prop="empNo" align="center" label="工号">
                <template slot-scope="scope">
                    <span>{{scope.row.empNo}}</span>
                </template>
            </el-table-column>
            <el-table-column prop="deptVo.name" align="center" label="部门">
                <template slot-scope="scope">
                    <span v-if="scope.row.deptVo">{{scope.row.deptVo.name}}</span>
                    <span v-else></span>
                </template>
            </el-table-column>
            <el-table-column prop="sex" align="center" label="性别">
                <template slot-scope="scope">
                    <span>{{scope.row.sex | enums('Gender')}}</span>
                </template>
            </el-table-column>
            <el-table-column align="center" label="电话">
                <template slot-scope="scope">
                    <span>{{scope.row.phone}}</span>
                </template>
            </el-table-column>
            <el-table-column class-name="status-col" label="帐号">
                <template slot-scope="scope">
                    <span>{{scope.row.account}}</span>
                </template>
            </el-table-column>
            <el-table-column prop="enable" class-name="status-col" label="状态">
                <template slot-scope="scope">
                    <el-tag :type="scope.row.enable | enums('Enable') | statusFilter">
                        {{scope.row.enable | enums('Enable')}}
                    </el-tag>
                </template>
            </el-table-column>
        </el-table>

        <div v-show="!listLoading" class="pagination-container">
            <el-pagination @size-change="handleSizeChange" @current-change="handleCurrentChange"
                           :current-page.sync="listQuery.page" :page-sizes="this.$store.state.app.pageSize"
                           :page-size="listQuery.rows" layout="total, sizes, prev, pager, next, jumper" :total="total">
            </el-pagination>
        </div>

        <el-dialog :title="textMap[dialogStatus]" :visible.sync="dialogFormVisible"
                   :close-on-click-modal="closeOnClickModal" :before-close="resetUserForm">
            <el-form id="checkboxTable" ref="userForm1" class="small-space" :model="sysUser" label-position="right"
                     label-width="80px"
                     style='width: 80%; margin-left:10%;' v-loading="dialogLoading" :rules="sysUserRules1">
                <el-form-item label="部门" prop="deptId">
                    <el-cascader :options="cascader" class="filter-item" @change="handleChanges" v-model="updateModel"
                                 :show-all-levels="true"
                                 :change-on-select="true" expand-trigger="hover" :clearable="true" style="width: 180px"
                                 placeholder="选择部门"
                    ></el-cascader>
                </el-form-item>
                <el-form-item label="姓名" prop="name">
                    <el-input v-model="sysUser.name"/>
                </el-form-item>
                <el-form-item label="工号" prop="empNo">
                    <el-input v-model="sysUser.empNo"/>
                </el-form-item>
                <el-form-item label="性别" prop="sex">
                    <el-radio-group v-model="sysUser.sex">
                        <el-radio v-for="item in enums['Gender']"
                                  :key="item.code"
                                  :label="item.code"
                                  :value="item.code">
                            <span style="font-weight:normal;">{{item.value}}</span>
                        </el-radio>
                    </el-radio-group>
                </el-form-item>
                <el-form-item label="电话" prop="phone">
                    <el-input v-model="sysUser.phone"/>
                </el-form-item>
                <el-form-item label="头像" prop="avatar">
                    <el-upload class="avatar-uploader" name="uploadFile"
                               :accept="acceptTypes"
                               :action="uploadAction"
                               :on-success="handleAvatarSuccess"
                               :on-error="handlerAvatarError"
                               :before-upload="beforeAvatarUpload"
                               :show-file-list="false"
                               :on-remove="handleRemove">
                        <img v-if="imageUrl" :src="imageUrl" class="avatar">
                        <i v-else class="el-icon-plus avatar-uploader-icon"></i>
                    </el-upload>
                </el-form-item>
                <el-form-item label="帐号" prop="account">
                    <el-input v-model="sysUser.account"/>
                </el-form-item>
                <el-form-item label="密码" prop="password">
                    <el-input v-model="sysUser.password" type="password" placeholder="修改密码时填入新密码，若不需要则无需输入"/>
                </el-form-item>
                <el-form-item label="确认密码" prop="passwordConfirm">
                    <el-input v-model="sysUser.passwordConfirm" type="password" placeholder="修改密码时填入新密码，若不需要则无需输入"/>
                </el-form-item>
                <el-form-item label="状态" prop="enable">
                    <el-radio-group v-model="sysUser.enable">
                        <el-radio v-for="item in enums['Enable']"
                                  :key="item.code"
                                  :label="item.code"
                                  :value="item.code">
                            <span style="font-weight:normal;">{{item.value}}</span>
                        </el-radio>
                    </el-radio-group>
                </el-form-item>
                <el-form-item label="备注">
                    <el-input type="textarea" v-model="sysUser.remark" :rows="3"/>
                </el-form-item>
            </el-form>
            <div slot="footer" class="dialog-footer">
                <el-button icon="circle-cross" type="danger" @click="resetUserForm">取 消</el-button>
                <el-button v-if="dialogStatus=='create'" type="primary" icon="circle-check" @click="doCreate">确 定
                </el-button>
                <el-button v-else type="primary" icon="circle-check" @Keyup.enter="doUpdate" @click="doUpdate">确 定
                </el-button>
            </div>
        </el-dialog>
    </div>
</template>


<script>
    import {mapGetters} from 'vuex';
    import {copyProperties, resetForm} from 'utils';
    import {validatMobiles} from 'utils/validate'
    import {getDeptCascader} from 'api/baseSystem/org/dept';
    import {getUserList, updateUser, createUser, delUser} from 'api/baseSystem/org/user';

    export default {
        name: 'table_demo',
        data() {
            const validateMobiles = (rule, value, callback) => {
                if (!validatMobiles(value)) {
                    callback(new Error('电话号码格式不正确'));
                } else {
                    callback();
                }
            };
            const validatePass = (rule, value, callback) => {
                if (this.sysUser.passwordConfirm) {
                    this.$refs.userForm1.validateField('passwordConfirm')
                }
                callback()
            };
            const validatePass2 = (rule, value, callback) => {
                if (this.sysUser.password && this.sysUser.passwordConfirm
                    && this.sysUser.password != this.sysUser.passwordConfirm) {
                    callback(new Error('两次输入密码不一致!'))
                }
                callback()
            };
            return {
                imageUrl: '',
                list: null,
                total: null,
                listLoading: true,
                listQuery: {
                    page: this.$store.state.app.page,
                    rows: this.$store.state.app.rows,
                    name: undefined,
                    deptId: undefined
                },
                sysUser: {
                    id: undefined,
                    deptId: '',
                    deptVo: {
                        id: '',
                        name: '',
                        treePosition: '',
                    },
                    name: '',
                    sex: 1,
                    phone: '',
                    avatar: '',
                    account: '',
                    password: '',
                    passwordConfirm: '',
                    enable: 1,
                    remark: '',
                    empNo: ''
                },
                sysUserRules1: {
                    deptId: [
                        {required: true, message: '请选择部门'}
                    ],
                    name: [
                        {required: true, message: '请输入姓名'}
                    ],
                    phone: [
                        {required: true, message: '请输入手机号码'},
                        {validator: validateMobiles, trigger: 'blur'}
                    ],
                    avatar: [
                        {required: true, message: '头像地址不正确'}
                    ],
                    account: [
                        {required: true, message: '请输入邮箱'},
                        {type: 'email', message: '请输入正确的邮箱地址', trigger: 'blur'}
                    ],
                    password: [
                        {required: true, message: '请输入密码'},
                        {min: 6, max: 18, message: '长度在 6 到 18 个字符', trigger: 'blur'},
                        {validator: validatePass, trigger: 'blur'}
                    ],
                    passwordConfirm: [
                        {required: true, message: '请再次输入密码'},
                        {validator: validatePass2, trigger: 'blur'}
                    ],
                    empNo: [
                        {required: true, message: '请输入工号'}
                    ]
                },
                selectedRows: [],
                cascader: [],
                dialogFormVisible: false,
                dialogStatus: '',
                dialogLoading: false,
                uploadAction: this.$store.state.app.uploadUrl,
                acceptTypes: this.$store.state.app.imageAccepts
            }
        },
        computed: {
            ...mapGetters([
                'textMap',
                'enums',
                'closeOnClickModal'
            ]),
            updateModel() {
                let result = [];
                if (this.sysUser.deptVo.treePosition) {
                    result = (this.sysUser.deptVo.treePosition + '&' + this.sysUser.deptVo.id).split('&');
                } else {
                    result = [this.sysUser.deptVo.id + ''];
                }
                return result;
            }
        },
        created() {
            this.getList();
            this.getOptions();
        },
        methods: {
            getList() {
                this.listLoading = true;
                getUserList(this.listQuery).then(response => {
                    if (response.httpCode !== 200) {
                        this.$message.error(response.msg);
                    } else {
                        if (response.data == null) {
                            this.list = response.data;
                            this.total = 0;
                        } else {
                            this.list = response.data.list;
                            this.total = response.data.total;
                        }
                    }
                    this.listLoading = false;
                })
            },
            getOptions(id) {
                getDeptCascader(id).then(response => {
                    if (response.httpCode === 200) {
                        this.cascader = response.data;
                    } else {
                        this.$message.error(response.msg);
                    }
                })
            },
            handleChange(value) {
                this.listQuery.deptId = null;
                if (value.length > 0) {
                    this.listQuery.deptId = value[value.length - 1];
                } else {
                    this.sysUser.deptId = 0;
                    this.getList();
                }
            },
            handleChanges(value) {
                if (value.length > 0) {
                    this.sysUser.deptId = value[value.length - 1];
                } else {
                    this.sysUser.deptId = 0;
                    this.getList();
                }
            },
            handleCreate(row) {
                this.currentRow = row;
                this.sysUserRules1.password[0].required = true;
                this.sysUserRules1.passwordConfirm[0].required = true;
                this.dialogStatus = 'create';
                this.dialogFormVisible = true;
            },
            handleUpdate(row) {
                this.currentRow = row;
                this.resetTemp();
                this.sysUser = copyProperties(this.sysUser, row);
                this.sysUser.password = '';
                this.sysUserRules1.password[0].required = false;
                this.sysUserRules1.passwordConfirm[0].required = false;
                this.imageUrl = this.sysUser.avatar;
                this.dialogStatus = 'update';
                this.dialogFormVisible = true;
            },
            handleDelete() {
                if (this.selectedRows.length === 0) {
                    this.$message.warning('请选择需要操作的记录');
                } else {
                    this.$confirm('此操作将永久删除该信息, 是否继续?', '提示', {
                        confirmButtonText: '确定',
                        cancelButtonText: '取消',
                        type: 'warning'
                    }).then(() => {
                        this.doDelete();
                    }).catch(() => {
                        console.dir('取消');
                    });
                }
            },
            doCreate() {
                this.$refs['userForm1'].validate(valid => {
                    if (valid) {
                        this.dialogLoading = true;
                        createUser(this.sysUser).then(response => {
                            this.dialogLoading = false;
                            if (response.httpCode !== 200) {
                                this.$message.error(response.msg);
                            } else {
                                this.resetUserForm();
                                this.$message.success('创建成功');
                                this.getList();
                            }
                        })
                    } else {
                        return false;
                    }
                });
            },
            doUpdate() {
                this.$refs['userForm1'].validate(valid => {
                    if (valid) {
                        this.dialogLoading = true;
                        this.sysUser.deptVo = {};
                        updateUser(this.sysUser).then(response => {
                            this.dialogLoading = false;
                            if (response.httpCode !== 200) {
                                this.$message.error(response.msg);
                            } else {
                                this.resetUserForm();
                                this.$message.success('更新成功');
                                this.getList();
                            }
                        })
                    } else {
                        return false;
                    }
                })
            },
            doDelete() {
                this.listLoading = true;
                let ids = [];
                for (const deleteRow of this.selectedRows) {
                    ids.push(deleteRow.id);
                }
                delUser(ids.join()).then(response => {
                    if (response.httpCode === 200) {
                        this.$message.success('删除成功！');
                        this.getList();
                    } else {
                        this.$message.error('删除失败！');
                    }
                    this.listLoading = false;
                })
            },
            handleAvatarSuccess(res, file, fileList) {
                if (res.state === 'SUCCESS') {
                    this.imageUrl = URL.createObjectURL(file.raw);
                    this.sysUser.avatar = res.url;
                    this.$message.success('上传成功！');
                } else {
                    this.$message.error('上传失败！');
                }
            },
            handlerAvatarError(err, file, fileList) {
                console.log(err);
                this.$message.error("网络不稳定，上传失败");
            },
            beforeAvatarUpload(file) {
                const isJPG = this.acceptTypes.includes(file.type);
                const isLt2M = file.size / 1024 / 1024 < 2;

                if (!isJPG) {
                    this.$message.error('上传头像图片格式不正确!');
                }
                if (!isLt2M) {
                    this.$message.error('上传头像图片大小不能超过 2MB!');
                }
                return isJPG && isLt2M;
            },
            handleRemove() {
                this.sysUser.avatar = '';
            },
            resetTemp() {
                this.sysUser = {
                    id: undefined,
                    deptId: '',
                    deptVo: {
                        id: '',
                        name: '',
                        treePosition: '',
                    },
                    name: '',
                    sex: 1,
                    phone: '',
                    avatar: '',
                    account: '',
                    password: '',
                    passwordConfirm: '',
                    enable: 1,
                    remark: '',
                    empNo: ''
                };
            },
            resetUserForm() {
                this.dialogFormVisible = false;
                this.resetTemp();
                this.imageUrl = '';
                resetForm(this, 'userForm1');
            },
            handleSizeChange(val) {
                this.listQuery.rows = val;
                this.listQuery.deptId = null;
                this.listQuery.name = null;
                this.getList();
            },
            handleCurrentChange(val) {
                this.listQuery.page = val;
                this.getList();
            },
            handleSelectionChange(rows) {
                this.selectedRows = rows;
            },
            toggleSelection(row) {
                this.$refs.userTable.toggleRowSelection(row);
            }
        }
    }
</script>
